def normalize_language(value: str | UserLanguage | None) -> UserLanguage:
    if isinstance(value, UserLanguage):
        return value
    if value in {UserLanguage.UZ.value, UserLanguage.RU.value, UserLanguage.EN.value}:
        return UserLanguage(value)
    return DEFAULT_LANGUAGE


def t(language: UserLanguage | str | None, key: str, **kwargs: Any) -> str:
    normalized_language = normalize_language(language)
    template = TRANSLATIONS.get(normalized_language, TRANSLATIONS[DEFAULT_LANGUAGE]).get(key)
    if template is None:
        template = TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    return template.format(**kwargs)


